export function asciiName(name: string): string {
  const chars = name.toLowerCase().split('');
  const rows: string[] = ['', '', '', '', ''];

  for (const ch of chars) {
    const letter = LETTERS[ch] ?? LETTERS[' ']!;
    for (let r = 0; r < 5; r++) {
      rows[r] += letter[r] + ' ';
    }
  }

  return rows.join('\n');
}
